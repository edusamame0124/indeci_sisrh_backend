-- ============================================================================
-- V010_77 — Módulo de Metas Presupuestales Anuales (6 tablas)
--
-- TABLAS:
--   1. INDECI_META_PPTO_CAT      — Catálogo anual único de metas presupuestales
--   2. INDECI_META_PPTO_LOTE     — Cabecera de proceso masivo
--   3. INDECI_EMP_META_ANUAL     — Asignación anual empleado ↔ meta
--   4. INDECI_META_PPTO_EQUIV    — Equivalencias entre años fiscales
--   5. INDECI_META_PPTO_LOTE_DET — Detalle / vista previa del proceso masivo
--   6. INDECI_META_PPTO_AUD      — Auditoría específica del módulo
--
-- DECISIONES DE DISEÑO (alineadas con V010_55 / V010_74 / V010_76):
--   - Naming INDECI_*; PKs IDENTITY (BY DEFAULT ON NULL); constraints INDECI_*_PK/FK/CK.
--   - TABLESPACE heredado de INDECI_EMPLEADO (evita ORA-01950/00959).
--   - Auditoría de filas: CREADO_POR/EN + MODIFICADO_POR/EN + ANULADO_POR/EN.
--     La aplicación (service layer) gestiona esos campos. Sin triggers.
--   - Auditoría centralizada: AuditoriaAspect (TBL_LOG_AUDITORIA, D.L. 1451).
--     INDECI_META_PPTO_AUD complementa con eventos de negocio del módulo.
--   - FK a INDECI_EMPLEADO referencia columna ID (no PERSONA_ID).
--   - UK del catálogo sobre META_HASH (SHA-256 de 7 campos) evita el límite
--     de tamaño de índice Oracle para UKs sobre múltiples VARCHAR2(500).
--   - UK funcional en INDECI_EMP_META_ANUAL: 1 asignación no-ANULADA/empleado/año.
--   - UK funcional en INDECI_META_PPTO_EQUIV: 1 equivalencia activa/origen/destino.
--   - NOMBRE INTENCIONALMENTE DISTINTO de INDECI_META_PRESUPUESTAL (V010_22),
--     que es el semáforo presupuestal del período — entidad diferente.
--   - Baja lógica: ESTADO='ANULADO' + MOTIVO_ANULACION. Sin DELETE físico.
--   - Sin ON DELETE CASCADE.
--
-- INTEGRACIÓN CON PLANILLA:
--   GeneradorPlanillaService lee INDECI_EMP_META_ANUAL filtrando:
--     EMPLEADO_ID = :empId AND ANIO_FISCAL = :anio
--     AND ESTADO = 'PUBLICADO' AND ESTADO <> 'ANULADO'
--   Al cerrar período: BLOQUEADO_POR_PLANILLA = 1 (no más modificaciones).
--
-- DEFENSA EN PROFUNDIDAD: idempotente (no falla si tablas/índices ya existen).
-- Ejecutar conectado como GESTIONRRHH / Oracle 19c+.
-- ============================================================================

SET SERVEROUTPUT ON;

-- ============================================================================
-- BLOQUE 1 — Tablas e índices
-- ============================================================================
DECLARE
    v_ts VARCHAR2(30);

    PROCEDURE add_table_if_missing(p_table VARCHAR2, p_ddl VARCHAR2) IS
        v NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v FROM USER_TABLES WHERE TABLE_NAME = p_table;
        IF v = 0 THEN
            EXECUTE IMMEDIATE p_ddl || ' TABLESPACE ' || v_ts;
            DBMS_OUTPUT.PUT_LINE(p_table || ' -> creada en TBS ' || v_ts);
        ELSE
            DBMS_OUTPUT.PUT_LINE(p_table || ' ya existe. Sin cambios.');
        END IF;
    END;

    PROCEDURE add_index_if_missing(p_idx VARCHAR2, p_ddl VARCHAR2) IS
        v NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v FROM USER_INDEXES WHERE INDEX_NAME = p_idx;
        IF v = 0 THEN
            EXECUTE IMMEDIATE p_ddl || ' TABLESPACE ' || v_ts;
            DBMS_OUTPUT.PUT_LINE(p_idx || ' -> creado.');
        ELSE
            DBMS_OUTPUT.PUT_LINE(p_idx || ' ya existe. Sin cambios.');
        END IF;
    END;

BEGIN
    -- Tablespace ancla: siempre de INDECI_EMPLEADO (tabla que sabemos que existe).
    SELECT TABLESPACE_NAME INTO v_ts
      FROM USER_TABLES
     WHERE TABLE_NAME = 'INDECI_EMPLEADO';

    DBMS_OUTPUT.PUT_LINE('Tablespace detectado: ' || v_ts);

    -- ==================================================================
    -- 1) INDECI_META_PPTO_CAT — Catálogo anual único de metas
    --    UK sobre META_HASH (SHA-256 de 7 campos) en lugar de UK directo
    --    sobre 6 columnas VARCHAR2(500) para evitar ORA-01450.
    -- ==================================================================
    add_table_if_missing(
        'INDECI_META_PPTO_CAT',
        'CREATE TABLE INDECI_META_PPTO_CAT ('
        || 'ID                     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,'
        || 'ANIO_FISCAL            NUMBER(4)     NOT NULL,'
        || 'META_CODIGO            VARCHAR2(30)  NOT NULL,'
        || 'CENTRO_COSTO           VARCHAR2(500) NOT NULL,'
        || 'CATEGORIA_PRESUPUESTAL VARCHAR2(500) NOT NULL,'
        || 'PRODUCTO               VARCHAR2(500) NOT NULL,'
        || 'ACTIVIDAD              VARCHAR2(500) NOT NULL,'
        || 'FINALIDAD              VARCHAR2(500) NOT NULL,'
        || 'META_HASH              VARCHAR2(64)  GENERATED ALWAYS AS ('
        ||   'RAWTOHEX(STANDARD_HASH('
        ||     'TO_CHAR(ANIO_FISCAL)||''|''||META_CODIGO||''|''||CENTRO_COSTO'
        ||     '||''|''||CATEGORIA_PRESUPUESTAL||''|''||PRODUCTO'
        ||     '||''|''||ACTIVIDAD||''|''||FINALIDAD'
        ||   ',''SHA256'')))'
        || ' VIRTUAL,'
        || 'ESTADO                 VARCHAR2(20)  DEFAULT ''BORRADOR'' NOT NULL,'
        || 'ACTIVO                 NUMBER(1)     DEFAULT 1 NOT NULL,'
        || 'FUENTE                 VARCHAR2(300),'
        || 'OBSERVACION            VARCHAR2(1000),'
        || 'CREADO_POR             VARCHAR2(100) NOT NULL,'
        || 'CREADO_EN              TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,'
        || 'MODIFICADO_POR         VARCHAR2(100),'
        || 'MODIFICADO_EN          TIMESTAMP,'
        || 'ANULADO_POR            VARCHAR2(100),'
        || 'ANULADO_EN             TIMESTAMP,'
        || 'MOTIVO_ANULACION       VARCHAR2(1000),'
        || 'CONSTRAINT INDECI_META_PPTO_CAT_PK        PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_META_PPTO_CAT_UK        UNIQUE (META_HASH),'
        || 'CONSTRAINT INDECI_META_PPTO_CAT_ESTADO_CK CHECK (ESTADO IN ('
        ||   '''BORRADOR'',''VALIDADO'',''PUBLICADO'',''CERRADO'',''ANULADO'')),'
        || 'CONSTRAINT INDECI_META_PPTO_CAT_ACTIVO_CK CHECK (ACTIVO IN (0,1)),'
        || 'CONSTRAINT INDECI_META_PPTO_CAT_ANIO_CK   CHECK (ANIO_FISCAL BETWEEN 2000 AND 2100)'
        || ')'
    );

    add_index_if_missing('IX_INDECI_META_CAT_ANIO',
        'CREATE INDEX IX_INDECI_META_CAT_ANIO '
        || 'ON INDECI_META_PPTO_CAT (ANIO_FISCAL)');
    add_index_if_missing('IX_INDECI_META_CAT_META',
        'CREATE INDEX IX_INDECI_META_CAT_META '
        || 'ON INDECI_META_PPTO_CAT (META_CODIGO)');
    add_index_if_missing('IX_INDECI_META_CAT_ESTADO',
        'CREATE INDEX IX_INDECI_META_CAT_ESTADO '
        || 'ON INDECI_META_PPTO_CAT (ANIO_FISCAL, ESTADO)');

    -- ==================================================================
    -- 2) INDECI_META_PPTO_LOTE — Cabecera de proceso masivo
    --    CODIGO_LOTE generado por la aplicación (UUID recomendado).
    -- ==================================================================
    add_table_if_missing(
        'INDECI_META_PPTO_LOTE',
        'CREATE TABLE INDECI_META_PPTO_LOTE ('
        || 'ID                   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,'
        || 'CODIGO_LOTE          VARCHAR2(50)  NOT NULL,'
        || 'ANIO_ORIGEN          NUMBER(4),'
        || 'ANIO_DESTINO         NUMBER(4)     NOT NULL,'
        || 'TIPO_PROCESO         VARCHAR2(40)  NOT NULL,'
        || 'ESTADO               VARCHAR2(30)  DEFAULT ''CREADO'' NOT NULL,'
        || 'TOTAL_EMPLEADOS      NUMBER        DEFAULT 0 NOT NULL,'
        || 'TOTAL_ASIGNADOS      NUMBER        DEFAULT 0 NOT NULL,'
        || 'TOTAL_OBSERVADOS     NUMBER        DEFAULT 0 NOT NULL,'
        || 'TOTAL_ERRORES        NUMBER        DEFAULT 0 NOT NULL,'
        || 'TOTAL_SIN_EQUIV      NUMBER        DEFAULT 0 NOT NULL,'
        || 'TOTAL_INACTIVOS      NUMBER        DEFAULT 0 NOT NULL,'
        || 'TOTAL_DUPLICADOS     NUMBER        DEFAULT 0 NOT NULL,'
        || 'ARCHIVO_ORIGEN       VARCHAR2(500),'
        || 'OBSERVACION          VARCHAR2(1000),'
        || 'CREADO_POR           VARCHAR2(100) NOT NULL,'
        || 'CREADO_EN            TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,'
        || 'FINALIZADO_EN        TIMESTAMP,'
        || 'ANULADO_POR          VARCHAR2(100),'
        || 'ANULADO_EN           TIMESTAMP,'
        || 'MOTIVO_ANULACION     VARCHAR2(1000),'
        || 'CONSTRAINT INDECI_META_PPTO_LOTE_PK      PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_META_PPTO_LOTE_COD_UK  UNIQUE (CODIGO_LOTE),'
        || 'CONSTRAINT INDECI_META_PPTO_LOTE_TIPO_CK CHECK (TIPO_PROCESO IN ('
        ||   '''CARGA_CATALOGO'',''COPIA_ANIO_ANTERIOR'',''APLICACION_EQUIVALENCIAS'','
        ||   '''IMPORTACION_EXCEL'',''PUBLICACION_ANUAL'',''REGULARIZACION'')),'
        || 'CONSTRAINT INDECI_META_PPTO_LOTE_EST_CK  CHECK (ESTADO IN ('
        ||   '''CREADO'',''PROCESANDO'',''VALIDADO'',''OBSERVADO'',''PUBLICADO'',''ANULADO'',''ERROR'')),'
        || 'CONSTRAINT INDECI_META_PPTO_LOTE_ANIO_CK CHECK (ANIO_DESTINO BETWEEN 2000 AND 2100)'
        || ')'
    );

    add_index_if_missing('IX_INDECI_META_LOTE_ANIO',
        'CREATE INDEX IX_INDECI_META_LOTE_ANIO '
        || 'ON INDECI_META_PPTO_LOTE (ANIO_DESTINO)');
    add_index_if_missing('IX_INDECI_META_LOTE_ESTADO',
        'CREATE INDEX IX_INDECI_META_LOTE_ESTADO '
        || 'ON INDECI_META_PPTO_LOTE (ESTADO)');
    add_index_if_missing('IX_INDECI_META_LOTE_TIPO',
        'CREATE INDEX IX_INDECI_META_LOTE_TIPO '
        || 'ON INDECI_META_PPTO_LOTE (TIPO_PROCESO, ESTADO)');

    -- ==================================================================
    -- 3) INDECI_EMP_META_ANUAL — Asignación anual empleado ↔ meta
    --    UK funcional: solo 1 asignación no-ANULADA por empleado por año.
    -- ==================================================================
    add_table_if_missing(
        'INDECI_EMP_META_ANUAL',
        'CREATE TABLE INDECI_EMP_META_ANUAL ('
        || 'ID                     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,'
        || 'EMPLEADO_ID            NUMBER        NOT NULL,'
