        || 'META_PPTO_CAT_ID       NUMBER        NOT NULL,'
        || 'VIGENCIA_INICIO        DATE          NOT NULL,'
        || 'VIGENCIA_FIN           DATE,'
        || 'ESTADO                 VARCHAR2(20)  DEFAULT ''BORRADOR'' NOT NULL,'
        || 'ORIGEN                 VARCHAR2(40)  DEFAULT ''MANUAL'' NOT NULL,'
        || 'LOTE_ID                NUMBER,'
        || 'BLOQUEADO_POR_PLANILLA NUMBER(1)     DEFAULT 0 NOT NULL,'
        || 'OBSERVACION            VARCHAR2(1000),'
        || 'CREADO_POR             VARCHAR2(100) NOT NULL,'
        || 'CREADO_EN              TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,'
        || 'MODIFICADO_POR         VARCHAR2(100),'
        || 'MODIFICADO_EN          TIMESTAMP,'
        || 'ANULADO_POR            VARCHAR2(100),'
        || 'ANULADO_EN             TIMESTAMP,'
        || 'MOTIVO_ANULACION       VARCHAR2(1000),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_PK      PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_EMP_FK  FOREIGN KEY (EMPLEADO_ID) '
        ||   'REFERENCES INDECI_EMPLEADO (ID),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_META_FK FOREIGN KEY (META_PPTO_CAT_ID) '
        ||   'REFERENCES INDECI_META_PPTO_CAT (ID),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_LOTE_FK FOREIGN KEY (LOTE_ID) '
        ||   'REFERENCES INDECI_META_PPTO_LOTE (ID),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_EST_CK  CHECK (ESTADO IN ('
        ||   '''BORRADOR'',''VALIDADO'',''PUBLICADO'',''CERRADO'',''ANULADO'',''OBSERVADO'')),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_ORI_CK  CHECK (ORIGEN IN ('
        ||   '''MANUAL'',''COPIA_ANIO_ANTERIOR'',''EQUIVALENCIA'','
        ||   '''IMPORTACION_EXCEL'',''REGULARIZACION'')),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_BLOQ_CK CHECK (BLOQUEADO_POR_PLANILLA IN (0,1)),'
        || 'CONSTRAINT INDECI_EMP_META_ANUAL_FECH_CK CHECK (VIGENCIA_FIN IS NULL OR VIGENCIA_FIN >= VIGENCIA_INICIO)'
        || ')'
    );

    add_index_if_missing('IX_INDECI_EMP_META_EMP',
        'CREATE INDEX IX_INDECI_EMP_META_EMP '
        || 'ON INDECI_EMP_META_ANUAL (EMPLEADO_ID)');
    add_index_if_missing('IX_INDECI_EMP_META_ANIO',
        'CREATE INDEX IX_INDECI_EMP_META_ANIO '
        || 'ON INDECI_EMP_META_ANUAL (ANIO_FISCAL)');
    add_index_if_missing('IX_INDECI_EMP_META_META',
        'CREATE INDEX IX_INDECI_EMP_META_META '
        || 'ON INDECI_EMP_META_ANUAL (META_PPTO_CAT_ID)');
    add_index_if_missing('IX_INDECI_EMP_META_ESTADO',
        'CREATE INDEX IX_INDECI_EMP_META_ESTADO '
        || 'ON INDECI_EMP_META_ANUAL (EMPLEADO_ID, ANIO_FISCAL, ESTADO)');
    add_index_if_missing('IX_INDECI_EMP_META_LOTE',
        'CREATE INDEX IX_INDECI_EMP_META_LOTE '
        || 'ON INDECI_EMP_META_ANUAL (LOTE_ID)');
    -- UK funcional: CASE devuelve NULL para ANULADO → no participa en unicidad
    add_index_if_missing('UK_INDECI_EMP_META_ACTIVA',
        'CREATE UNIQUE INDEX UK_INDECI_EMP_META_ACTIVA '
        || 'ON INDECI_EMP_META_ANUAL ('
        ||   'CASE WHEN ESTADO <> ''ANULADO'' THEN EMPLEADO_ID END,'
        ||   'CASE WHEN ESTADO <> ''ANULADO'' THEN ANIO_FISCAL  END'
        || ')');

    -- ==================================================================
    -- 4) INDECI_META_PPTO_EQUIV — Equivalencias entre años fiscales
    --    UK funcional: 1 equivalencia activa por (origen, año destino).
    -- ==================================================================
    add_table_if_missing(
        'INDECI_META_PPTO_EQUIV',
        'CREATE TABLE INDECI_META_PPTO_EQUIV ('
        || 'ID               NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,'
        || 'ANIO_ORIGEN      NUMBER(4)     NOT NULL,'
        || 'META_ORIGEN_ID   NUMBER        NOT NULL,'
        || 'ANIO_DESTINO     NUMBER(4)     NOT NULL,'
        || 'META_DESTINO_ID  NUMBER        NOT NULL,'
        || 'ESTADO           VARCHAR2(20)  DEFAULT ''BORRADOR'' NOT NULL,'
        || 'ACTIVO           NUMBER(1)     DEFAULT 1 NOT NULL,'
        || 'OBSERVACION      VARCHAR2(1000),'
        || 'CREADO_POR       VARCHAR2(100) NOT NULL,'
        || 'CREADO_EN        TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,'
        || 'MODIFICADO_POR   VARCHAR2(100),'
        || 'MODIFICADO_EN    TIMESTAMP,'
        || 'ANULADO_POR      VARCHAR2(100),'
        || 'ANULADO_EN       TIMESTAMP,'
        || 'MOTIVO_ANULACION VARCHAR2(1000),'
        || 'CONSTRAINT INDECI_META_PPTO_EQUIV_PK      PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_META_PPTO_EQUIV_ORI_FK  FOREIGN KEY (META_ORIGEN_ID) '
        ||   'REFERENCES INDECI_META_PPTO_CAT (ID),'
        || 'CONSTRAINT INDECI_META_PPTO_EQUIV_DEST_FK FOREIGN KEY (META_DESTINO_ID) '
        ||   'REFERENCES INDECI_META_PPTO_CAT (ID),'
        || 'CONSTRAINT INDECI_META_PPTO_EQUIV_EST_CK  CHECK (ESTADO IN ('
        ||   '''BORRADOR'',''VALIDADO'',''PUBLICADO'',''ANULADO'')),'
        || 'CONSTRAINT INDECI_META_PPTO_EQUIV_ACT_CK  CHECK (ACTIVO IN (0,1)),'
        || 'CONSTRAINT INDECI_META_PPTO_EQUIV_ANIO_CK CHECK (ANIO_DESTINO > ANIO_ORIGEN)'
        || ')'
    );

    add_index_if_missing('IX_INDECI_META_EQUIV_ORI',
        'CREATE INDEX IX_INDECI_META_EQUIV_ORI '
        || 'ON INDECI_META_PPTO_EQUIV (ANIO_ORIGEN, META_ORIGEN_ID)');
    add_index_if_missing('IX_INDECI_META_EQUIV_DEST',
        'CREATE INDEX IX_INDECI_META_EQUIV_DEST '
        || 'ON INDECI_META_PPTO_EQUIV (ANIO_DESTINO, META_DESTINO_ID)');
    add_index_if_missing('IX_INDECI_META_EQUIV_ESTADO',
        'CREATE INDEX IX_INDECI_META_EQUIV_ESTADO '
        || 'ON INDECI_META_PPTO_EQUIV (ESTADO, ACTIVO)');
    -- UK funcional: 1 equivalencia ACTIVA/no-ANULADA por (origen → año destino)
    add_index_if_missing('UK_INDECI_META_EQUIV_ACT',
        'CREATE UNIQUE INDEX UK_INDECI_META_EQUIV_ACT '
        || 'ON INDECI_META_PPTO_EQUIV ('
        ||   'CASE WHEN ACTIVO = 1 AND ESTADO <> ''ANULADO'' THEN ANIO_ORIGEN    END,'
        ||   'CASE WHEN ACTIVO = 1 AND ESTADO <> ''ANULADO'' THEN META_ORIGEN_ID END,'
        ||   'CASE WHEN ACTIVO = 1 AND ESTADO <> ''ANULADO'' THEN ANIO_DESTINO   END'
        || ')');

    -- ==================================================================
    -- 5) INDECI_META_PPTO_LOTE_DET — Detalle del proceso masivo
    --    Vista previa + validación por empleado antes de publicar.
    -- ==================================================================
    add_table_if_missing(
        'INDECI_META_PPTO_LOTE_DET',
        'CREATE TABLE INDECI_META_PPTO_LOTE_DET ('
        || 'ID                 NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,'
        || 'LOTE_ID            NUMBER        NOT NULL,'
        || 'EMPLEADO_ID        NUMBER        NOT NULL,'
        || 'META_ORIGEN_ID     NUMBER,'
        || 'META_DESTINO_ID    NUMBER,'
        || 'EMP_META_ANUAL_ID  NUMBER,'
        || 'ESTADO_VALIDACION  VARCHAR2(40)  DEFAULT ''OK'' NOT NULL,'
