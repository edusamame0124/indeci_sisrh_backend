-- ============================================================================
-- Spec 010 / V010_01 — INDECI_PARAMETRO_REMUNERATIVO
-- Tabla maestra de parámetros remunerativos vigentes por año fiscal y régimen.
-- Cumple REGLA-02 (no hardcodear) y soporta auditoría (¿con qué tasa se calculó?).
-- Ejecutar en esquema GESTIONRRHH / Oracle.
-- ============================================================================

CREATE TABLE GESTIONRRHH.INDECI_PARAMETRO_REMUNERATIVO (
    ID                   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CODIGO_PARAMETRO     VARCHAR2(60 CHAR)  NOT NULL,
    ANIO_FISCAL          NUMBER(4)          NOT NULL,
    REGIMEN_LABORAL_ID   NUMBER             NULL,
    VALOR_NUMERICO       NUMBER(18,6)       NOT NULL,
    UNIDAD               VARCHAR2(20 CHAR)  NULL,
    FECHA_VIG_INI        DATE               DEFAULT SYSDATE NOT NULL,
    FECHA_VIG_FIN        DATE               NULL,
    ACTIVO               NUMBER(1)          DEFAULT 1 NOT NULL,
    CREATED_AT           TIMESTAMP          DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT INDECI_PARAM_REM_UK
        UNIQUE (CODIGO_PARAMETRO, ANIO_FISCAL, REGIMEN_LABORAL_ID),
    CONSTRAINT INDECI_PARAM_REM_ACTIVO_CK
        CHECK (ACTIVO IN (0, 1)),
    CONSTRAINT INDECI_PARAM_REM_UNIDAD_CK
        CHECK (UNIDAD IS NULL OR UNIDAD IN ('PEN', 'PCT', 'MIN', 'DIAS'))
);

CREATE INDEX IX_INDECI_PARAM_REM_LOOKUP
    ON GESTIONRRHH.INDECI_PARAMETRO_REMUNERATIVO (CODIGO_PARAMETRO, ANIO_FISCAL, ACTIVO);

COMMENT ON TABLE  GESTIONRRHH.INDECI_PARAMETRO_REMUNERATIVO IS
    'Spec 010 — Parámetros remunerativos (UIT, RMV, tasas, montos) por año fiscal y régimen.';
COMMENT ON COLUMN GESTIONRRHH.INDECI_PARAMETRO_REMUNERATIVO.CODIGO_PARAMETRO IS
    'Código semántico del parámetro: UIT, RMV, TASA_ONP, TASA_ESSALUD, etc.';
COMMENT ON COLUMN GESTIONRRHH.INDECI_PARAMETRO_REMUNERATIVO.REGIMEN_LABORAL_ID IS
    'FK a INDECI_REGIMEN_LABORAL.ID. NULL = aplica a todos los regímenes.';
COMMENT ON COLUMN GESTIONRRHH.INDECI_PARAMETRO_REMUNERATIVO.VALOR_NUMERICO IS
    'Valor del parámetro. Interpretación según UNIDAD (PEN, PCT, MIN, DIAS).';
COMMENT ON COLUMN GESTIONRRHH.INDECI_PARAMETRO_REMUNERATIVO.UNIDAD IS
    'PEN=soles, PCT=porcentaje [0..1], MIN=minutos, DIAS=días.';
