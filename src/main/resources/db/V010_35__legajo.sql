-- ----------------------------------------------------------------------------
-- 2) Índices para las queries de los repos.
--    LegajoDocumentoRepository.findByEmpleadoIdAndActivoOrderByCreatedAtDesc
--    LegajoDocumentoRepository.findByEmpleadoIdAndCategoriaIdAndActivo...
--    LegajoDocumentoRepository.findByEmpleadoIdAndSubcategoriaIdAndActivo...
--    LegajoSubcategoriaRepository.findByCategoriaIdAndActivoOrderBy...
-- ----------------------------------------------------------------------------
-- El índice hereda el TABLESPACE de la tabla padre (USER_TABLES.TABLESPACE_NAME).
-- Esto evita ORA-00959 cuando el default tablespace del usuario no existe o no
-- tiene cuota.
DECLARE
    PROCEDURE add_index_if_missing(
        p_index_name VARCHAR2,
        p_table_name VARCHAR2,
        p_columns    VARCHAR2   -- e.g. '(EMPLEADO_ID, ACTIVO)'
    ) IS
        v_exists NUMBER;
        v_ts     VARCHAR2(30);
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_INDEXES
         WHERE INDEX_NAME = p_index_name;
        IF v_exists > 0 THEN
            DBMS_OUTPUT.PUT_LINE(p_index_name || ' ya existe. Sin cambios.');
            RETURN;
        END IF;

        SELECT TABLESPACE_NAME INTO v_ts
          FROM USER_TABLES
         WHERE TABLE_NAME = p_table_name;

        EXECUTE IMMEDIATE
            'CREATE INDEX ' || p_index_name
            || ' ON ' || p_table_name || ' ' || p_columns
            || ' TABLESPACE ' || v_ts;
        DBMS_OUTPUT.PUT_LINE(p_index_name || ' -> creado en TBS ' || v_ts);
    END;
BEGIN
    add_index_if_missing(
        'INDECI_LEGAJO_DOC_EMP_IX',
        'INDECI_LEGAJO_DOCUMENTO',
        '(EMPLEADO_ID, ACTIVO)'
    );
    add_index_if_missing(
        'INDECI_LEGAJO_DOC_CAT_IX',
        'INDECI_LEGAJO_DOCUMENTO',
        '(EMPLEADO_ID, CATEGORIA_ID, ACTIVO)'
    );
    add_index_if_missing(
        'INDECI_LEGAJO_DOC_SUBCAT_IX',
        'INDECI_LEGAJO_DOCUMENTO',
        '(SUBCATEGORIA_ID, ACTIVO)'
    );
    add_index_if_missing(
        'INDECI_LEGAJO_SUBCAT_CAT_IX',
        'INDECI_LEGAJO_SUBCATEGORIA',
        '(CATEGORIA_ID, ACTIVO)'
    );
    DBMS_OUTPUT.PUT_LINE('V010_35 índices listos.');
END;
/

-- ----------------------------------------------------------------------------
-- 3) Seed categorías base (idempotente). RRHH puede ampliar.
-- ----------------------------------------------------------------------------
MERGE INTO INDECI_LEGAJO_CATEGORIA d
USING (
    SELECT 'Contrato'          AS NOMBRE, 10  AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT 'Documento de identidad' AS NOMBRE, 20  AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT 'Certificados'      AS NOMBRE, 30  AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT 'Subsidios'         AS NOMBRE, 40  AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT 'Permisos y licencias' AS NOMBRE, 50  AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT 'Resoluciones'      AS NOMBRE, 60  AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT 'Otros'             AS NOMBRE, 99  AS ORDEN_VISUAL FROM DUAL
) s
ON (d.NOMBRE = s.NOMBRE)
WHEN NOT MATCHED THEN
    INSERT (NOMBRE, ORDEN_VISUAL, ACTIVO, CREATED_AT)
    VALUES (s.NOMBRE, s.ORDEN_VISUAL, 1, SYSTIMESTAMP);

-- ----------------------------------------------------------------------------
-- 4) Seed subcategorías base (idempotente). Se siembran solo las imprescindibles
--    para F2 (Eventos del Período).
-- ----------------------------------------------------------------------------
MERGE INTO INDECI_LEGAJO_SUBCATEGORIA d
USING (
    -- bajo Subsidios
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Subsidios') AS CATEGORIA_ID,
           'Maternidad' AS NOMBRE, 10 AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Subsidios') AS CATEGORIA_ID,
           'Enfermedad' AS NOMBRE, 20 AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Subsidios') AS CATEGORIA_ID,
           'Lactancia'  AS NOMBRE, 30 AS ORDEN_VISUAL FROM DUAL UNION ALL
    -- bajo Permisos y licencias
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Permisos y licencias') AS CATEGORIA_ID,
           'Licencia con goce'    AS NOMBRE, 10 AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Permisos y licencias') AS CATEGORIA_ID,
           'Licencia sin goce'    AS NOMBRE, 20 AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Permisos y licencias') AS CATEGORIA_ID,
           'Permiso personal'     AS NOMBRE, 30 AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Permisos y licencias') AS CATEGORIA_ID,
           'Paternidad'           AS NOMBRE, 40 AS ORDEN_VISUAL FROM DUAL UNION ALL
    -- bajo Certificados
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Certificados') AS CATEGORIA_ID,
           'Médico'   AS NOMBRE, 10 AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT (SELECT ID FROM INDECI_LEGAJO_CATEGORIA WHERE NOMBRE='Certificados') AS CATEGORIA_ID,
           'Estudios' AS NOMBRE, 20 AS ORDEN_VISUAL FROM DUAL
) s
ON (d.CATEGORIA_ID = s.CATEGORIA_ID AND d.NOMBRE = s.NOMBRE)
WHEN NOT MATCHED THEN
    INSERT (CATEGORIA_ID, NOMBRE, ORDEN_VISUAL, ACTIVO, CREATED_AT)
    VALUES (s.CATEGORIA_ID, s.NOMBRE, s.ORDEN_VISUAL, 1, SYSTIMESTAMP);

COMMIT;
