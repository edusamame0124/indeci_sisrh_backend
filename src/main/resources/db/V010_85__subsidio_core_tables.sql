        SELECT COUNT(*) INTO v_ts_exists
          FROM USER_TABLESPACES
         WHERE TABLESPACE_NAME = v_ts;
        IF v_ts_exists = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Tablespace ancla ' || v_ts || ' no existe; se intenta default del usuario.');
            v_ts := NULL;
        END IF;
    END IF;

