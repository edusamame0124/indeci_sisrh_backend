-- ----------------------------------------------------------------------------
-- 2) ALTER TABLE idempotente: agregar columnas si no existen.
--    a) INDECI_EMPLEADO_PUESTO_HIST.CARGO_ID  → FK INDECI_CARGO
--    b) INDECI_OFICINA.ESTRUCTURA_ORGANICA_ID → FK INDECI_ESTRUCTURA_ORGANICA
-- ----------------------------------------------------------------------------
DECLARE
    PROCEDURE add_column_if_missing(
        p_table_name  VARCHAR2,
        p_column_name VARCHAR2,
        p_column_ddl  VARCHAR2
    ) IS
        v_exists NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_TAB_COLUMNS
         WHERE TABLE_NAME = p_table_name
           AND COLUMN_NAME = p_column_name;
        IF v_exists = 0 THEN
            EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name
                || ' ADD ' || p_column_ddl;
            DBMS_OUTPUT.PUT_LINE(p_table_name || '.' || p_column_name || ' -> agregada.');
        ELSE
            DBMS_OUTPUT.PUT_LINE(p_table_name || '.' || p_column_name || ' ya existe. Sin cambios.');
        END IF;
    END;

    PROCEDURE add_fk_if_missing(
        p_table_name      VARCHAR2,
        p_constraint_name VARCHAR2,
        p_constraint_ddl  VARCHAR2
    ) IS
        v_exists NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_CONSTRAINTS
         WHERE TABLE_NAME = p_table_name
           AND CONSTRAINT_NAME = p_constraint_name;
        IF v_exists = 0 THEN
            EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name
                || ' ADD CONSTRAINT ' || p_constraint_name || ' ' || p_constraint_ddl;
            DBMS_OUTPUT.PUT_LINE(p_constraint_name || ' -> creado.');
        ELSE
            DBMS_OUTPUT.PUT_LINE(p_constraint_name || ' ya existe. Sin cambios.');
        END IF;
    END;
BEGIN
    -- 2a) EMPLEADO_PUESTO_HIST.CARGO_ID
    add_column_if_missing(
        'INDECI_EMPLEADO_PUESTO_HIST',
        'CARGO_ID',
        'CARGO_ID NUMBER(10)'
    );
    add_fk_if_missing(
        'INDECI_EMPLEADO_PUESTO_HIST',
        'INDECI_EMP_PUESTO_CARGO_FK',
        'FOREIGN KEY (CARGO_ID) REFERENCES INDECI_CARGO (ID) ENABLE NOVALIDATE'
    );

    -- 2b) OFICINA.ESTRUCTURA_ORGANICA_ID
    add_column_if_missing(
        'INDECI_OFICINA',
        'ESTRUCTURA_ORGANICA_ID',
        'ESTRUCTURA_ORGANICA_ID NUMBER(10)'
    );
    add_fk_if_missing(
        'INDECI_OFICINA',
        'INDECI_OFICINA_ESTRUCTURA_FK',
        'FOREIGN KEY (ESTRUCTURA_ORGANICA_ID) REFERENCES INDECI_ESTRUCTURA_ORGANICA (ID) ENABLE NOVALIDATE'
    );

    DBMS_OUTPUT.PUT_LINE('V010_34 ALTERs aplicados.');
END;
