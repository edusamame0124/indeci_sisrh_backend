-- ----------------------------------------------------------------------------
-- 3) Índices de soporte para los joins lazy del nuevo Cargo/Oficina mapping.
-- ----------------------------------------------------------------------------
-- El índice hereda el TABLESPACE de la tabla padre (USER_TABLES.TABLESPACE_NAME).
-- Esto evita ORA-00959 cuando el default tablespace del usuario no existe.
DECLARE
    PROCEDURE add_index_if_missing(
        p_index_name VARCHAR2,
        p_table_name VARCHAR2,
        p_columns    VARCHAR2   -- e.g. '(CARGO_ID)'
    ) IS
        v_exists NUMBER;
        v_ts     VARCHAR2(30);
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_INDEXES
         WHERE INDEX_NAME = p_index_name;
        IF v_exists > 0 THEN
            DBMS_OUTPUT.PUT_LINE(p_index_name || ' ya existe. Sin cambios.');
            RETURN;
        END IF;

        SELECT TABLESPACE_NAME INTO v_ts
          FROM USER_TABLES
         WHERE TABLE_NAME = p_table_name;

        EXECUTE IMMEDIATE
            'CREATE INDEX ' || p_index_name
            || ' ON ' || p_table_name || ' ' || p_columns
            || ' TABLESPACE ' || v_ts;
        DBMS_OUTPUT.PUT_LINE(p_index_name || ' -> creado en TBS ' || v_ts);
    END;
BEGIN
    add_index_if_missing(
        'INDECI_EMP_PUESTO_CARGO_IX',
        'INDECI_EMPLEADO_PUESTO_HIST',
        '(CARGO_ID)'
    );
    add_index_if_missing(
        'INDECI_CARGO_TIPO_IX',
        'INDECI_CARGO',
        '(TIPO_CARGO_ID)'
    );
    add_index_if_missing(
        'INDECI_OFICINA_ESTRUCTURA_IX',
        'INDECI_OFICINA',
        '(ESTRUCTURA_ORGANICA_ID)'
    );
    DBMS_OUTPUT.PUT_LINE('V010_34 índices listos.');
END;
/

-- ----------------------------------------------------------------------------
-- 4) Seed mínimo de TIPO_CARGO (idempotente).
--    RRHH puede ampliar por UI; mantener WHEN NOT MATCHED (no pisar lo cargado).
-- ----------------------------------------------------------------------------
MERGE INTO INDECI_TIPO_CARGO d
USING (
    SELECT 'NOMBRADO'    AS CODIGO, 'Nombrado'                AS NOMBRE FROM DUAL UNION ALL
    SELECT 'CONTRATADO'  AS CODIGO, 'Contratado por D.Leg.'   AS NOMBRE FROM DUAL UNION ALL
    SELECT 'CAS'         AS CODIGO, 'CAS (D.Leg. 1057)'       AS NOMBRE FROM DUAL UNION ALL
    SELECT 'FUNCIONARIO' AS CODIGO, 'Funcionario'             AS NOMBRE FROM DUAL UNION ALL
    SELECT 'ENCARGADO'   AS CODIGO, 'Encargado'               AS NOMBRE FROM DUAL UNION ALL
    SELECT 'PRACTICANTE' AS CODIGO, 'Practicante'             AS NOMBRE FROM DUAL UNION ALL
    SELECT 'SERVIR'      AS CODIGO, 'Régimen SERVIR'          AS NOMBRE FROM DUAL
) s
ON (d.CODIGO = s.CODIGO)
WHEN NOT MATCHED THEN
    INSERT (CODIGO, NOMBRE, ACTIVO, CREATED_AT)
    VALUES (s.CODIGO, s.NOMBRE, 1, SYSTIMESTAMP);

COMMIT;
