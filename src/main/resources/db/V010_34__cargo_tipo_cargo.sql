-- ----------------------------------------------------------------------------
-- 1) Tablas nuevas (patrón add_table_if_missing).
-- ----------------------------------------------------------------------------
-- Tablas heredan el TABLESPACE de INDECI_EMPLEADO (ancla estable que sabemos
-- existe en el schema). Evita ORA-00959 cuando el default tablespace del
-- usuario está vacío o roto (caso TBS_RRHH no existe).
DECLARE
    v_ts VARCHAR2(30);
    PROCEDURE add_table_if_missing(
        p_table_name VARCHAR2,
        p_create_ddl VARCHAR2
    ) IS
        v_exists NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_TABLES
         WHERE TABLE_NAME = p_table_name;
        IF v_exists = 0 THEN
            EXECUTE IMMEDIATE p_create_ddl || ' TABLESPACE ' || v_ts;
            DBMS_OUTPUT.PUT_LINE(p_table_name || ' -> creada en TBS ' || v_ts);
        ELSE
            DBMS_OUTPUT.PUT_LINE(p_table_name || ' ya existe. Sin cambios.');
        END IF;
    END;
BEGIN
    SELECT TABLESPACE_NAME INTO v_ts
      FROM USER_TABLES
     WHERE TABLE_NAME = 'INDECI_EMPLEADO';
    add_table_if_missing(
        'INDECI_TIPO_CARGO',
        'CREATE TABLE INDECI_TIPO_CARGO ('
        ||   'ID         NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,'
        ||   'CODIGO     VARCHAR2(20 CHAR),'
        ||   'NOMBRE     VARCHAR2(100 CHAR) NOT NULL,'
        ||   'ACTIVO     NUMBER(1) DEFAULT 1 NOT NULL,'
        ||   'CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        ||   'CONSTRAINT INDECI_TIPO_CARGO_PK PRIMARY KEY (ID),'
        ||   'CONSTRAINT INDECI_TIPO_CARGO_CODIGO_UK UNIQUE (CODIGO),'
        ||   'CONSTRAINT INDECI_TIPO_CARGO_ACTIVO_CK CHECK (ACTIVO IN (0, 1))'
        || ')'
    );

    add_table_if_missing(
        'INDECI_CARGO',
        'CREATE TABLE INDECI_CARGO ('
        ||   'ID            NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,'
        ||   'TIPO_CARGO_ID NUMBER(10),'
        ||   'CODIGO        VARCHAR2(20 CHAR),'
        ||   'NOMBRE        VARCHAR2(150 CHAR) NOT NULL,'
        ||   'ACTIVO        NUMBER(1) DEFAULT 1 NOT NULL,'
        ||   'CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        ||   'CONSTRAINT INDECI_CARGO_PK PRIMARY KEY (ID),'
        ||   'CONSTRAINT INDECI_CARGO_TIPO_FK FOREIGN KEY (TIPO_CARGO_ID) '
        ||      'REFERENCES INDECI_TIPO_CARGO (ID),'
        ||   'CONSTRAINT INDECI_CARGO_ACTIVO_CK CHECK (ACTIVO IN (0, 1))'
        || ')'
    );

    EXECUTE IMMEDIATE 'COMMENT ON TABLE INDECI_TIPO_CARGO IS '
        || '''F0.2 — Catálogo de tipos de cargo (Nombrado / Contratado / CAS / Funcionario / etc.). Pull 2026-05-31.''';
    EXECUTE IMMEDIATE 'COMMENT ON TABLE INDECI_CARGO IS '
        || '''F0.2 — Catálogo de cargos. Soporte de Cargo entity + validación en EmpleadoPuestoService. Pull 2026-05-31.''';

    DBMS_OUTPUT.PUT_LINE('V010_34 tablas listas.');
END;
