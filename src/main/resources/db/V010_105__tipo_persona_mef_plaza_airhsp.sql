SET SERVEROUTPUT ON;

DECLARE
    v_count NUMBER;
    v_ts VARCHAR2(30);
BEGIN
    SELECT MAX(tablespace_name) INTO v_ts FROM user_tables;
    IF v_ts IS NULL THEN v_ts := 'USERS'; END IF;

    SELECT count(*) INTO v_count FROM user_tables WHERE table_name = 'INDECI_TIPO_PERSONA_MEF';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE
        'CREATE TABLE GESTIONRRHH.INDECI_TIPO_PERSONA_MEF (
            ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            CODIGO VARCHAR2(10) NOT NULL,
            NOMBRE VARCHAR2(100) NOT NULL,
            DESCRIPCION VARCHAR2(500),
            ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,
            FECHA_REGISTRO TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
        ) TABLESPACE ' || v_ts;
        DBMS_OUTPUT.PUT_LINE('Tabla INDECI_TIPO_PERSONA_MEF creada con éxito.');
    ELSE
        DBMS_OUTPUT.PUT_LINE('La tabla INDECI_TIPO_PERSONA_MEF ya existe.');
    END IF;
    
    -- Agregar columnas a INDECI_EMPLEADO_PLANILLA
    SELECT count(*) INTO v_count FROM user_tab_columns WHERE table_name = 'INDECI_EMPLEADO_PLANILLA' AND column_name = 'TIPO_PERSONA_MEF_ID';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE GESTIONRRHH.INDECI_EMPLEADO_PLANILLA ADD TIPO_PERSONA_MEF_ID NUMBER';
        DBMS_OUTPUT.PUT_LINE('Columna TIPO_PERSONA_MEF_ID agregada a INDECI_EMPLEADO_PLANILLA.');
        
        EXECUTE IMMEDIATE 'ALTER TABLE GESTIONRRHH.INDECI_EMPLEADO_PLANILLA ADD CONSTRAINT FK_EMP_PLA_TIPO_PERSONA FOREIGN KEY (TIPO_PERSONA_MEF_ID) REFERENCES GESTIONRRHH.INDECI_TIPO_PERSONA_MEF(ID)';
    END IF;

    SELECT count(*) INTO v_count FROM user_tab_columns WHERE table_name = 'INDECI_EMPLEADO_PLANILLA' AND column_name = 'REGISTRO_PLAZA_AIRHSP';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE GESTIONRRHH.INDECI_EMPLEADO_PLANILLA ADD REGISTRO_PLAZA_AIRHSP VARCHAR2(50)';
        DBMS_OUTPUT.PUT_LINE('Columna REGISTRO_PLAZA_AIRHSP agregada a INDECI_EMPLEADO_PLANILLA.');
    END IF;

END;
