-- Defensa:
--   - Tablespace heredado de INDECI_EMPLEADO (evita ORA-01950/00959).
--   - Si la tabla existe VACIA, DROP TABLE CASCADE CONSTRAINTS PURGE (no DROP INDEX
--     suelto: ORA-02429 en indice PK). Recrea tabla + secuencia IDENTITY correcta.
--   - Seeds con ID explicito + restart identity.
-- Ejecutar conectado como GESTIONRRHH / Oracle 19c+.
-- ============================================================================

SET SERVEROUTPUT ON;

-- ----------------------------------------------------------------------------
-- 1) Reparacion + tabla principal
-- ----------------------------------------------------------------------------
DECLARE
    v_ts     VARCHAR2(30);
    v_exists NUMBER;
    v_rows   NUMBER;

    PROCEDURE eliminar_tabla_vacia IS
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_TABLES
         WHERE TABLE_NAME = 'INDECI_ESSALUD_VIGENCIA';
        IF v_exists = 0 THEN
            RETURN;
        END IF;

        EXECUTE IMMEDIATE 'SELECT COUNT(*) FROM INDECI_ESSALUD_VIGENCIA' INTO v_rows;
        IF v_rows > 0 THEN
            DBMS_OUTPUT.PUT_LINE(
                'INDECI_ESSALUD_VIGENCIA tiene ' || v_rows || ' filas. No se elimina.'
            );
            RETURN;
        END IF;

        -- CASCADE CONSTRAINTS: elimina PK, indices secundarios y secuencia IDENTITY.
        -- No usar DROP INDEX previo (ORA-02429 sobre indice de PK).
        EXECUTE IMMEDIATE 'DROP TABLE INDECI_ESSALUD_VIGENCIA CASCADE CONSTRAINTS PURGE';
        DBMS_OUTPUT.PUT_LINE(
            'Tabla vacia eliminada (CASCADE CONSTRAINTS): secuencia IDENTITY en USERS corregida.'
        );
    END;

    PROCEDURE crear_tabla_si_falta(p_tabla VARCHAR2, p_ddl VARCHAR2) IS
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_TABLES
         WHERE TABLE_NAME = UPPER(p_tabla);
        IF v_exists = 0 THEN
            EXECUTE IMMEDIATE p_ddl || ' TABLESPACE ' || v_ts;
            DBMS_OUTPUT.PUT_LINE(p_tabla || ' -> creada en TBS ' || v_ts);
        ELSE
            DBMS_OUTPUT.PUT_LINE(p_tabla || ' ya existe. Sin cambios.');
        END IF;
    END;

BEGIN
    SELECT TABLESPACE_NAME INTO v_ts
      FROM USER_TABLES
     WHERE TABLE_NAME = 'INDECI_EMPLEADO';

    DBMS_OUTPUT.PUT_LINE('Tablespace detectado: ' || v_ts);

    eliminar_tabla_vacia();

    crear_tabla_si_falta(
        'INDECI_ESSALUD_VIGENCIA',
        'CREATE TABLE INDECI_ESSALUD_VIGENCIA ('
        || 'ID                     NUMBER GENERATED BY DEFAULT AS IDENTITY,'
        || 'ANIO_VIGENCIA          NUMBER(4)     NOT NULL,'
        || 'VIGENCIA_INICIO        DATE          NOT NULL,'
        || 'VIGENCIA_FIN           DATE,'
        || 'UIT_VIGENTE            NUMBER(10,2)  NOT NULL,'
        || 'PCT_BASE_CAS           NUMBER(6,4)   DEFAULT 45    NOT NULL,'
        || 'PCT_ESSALUD            NUMBER(6,4)   DEFAULT 9     NOT NULL,'
        || 'PCT_ESSALUD_EPS        NUMBER(6,4)   DEFAULT 6.75  NOT NULL,'
        || 'PCT_CREDITO_EPS        NUMBER(6,4)   DEFAULT 2.25  NOT NULL,'
        || 'FUENTE_OFICIAL         VARCHAR2(500) NOT NULL,'
        || 'URL_FUENTE_OFICIAL     VARCHAR2(1000),'
        || 'FECHA_PUBLICACION      DATE,'
        || 'OBSERVACION            VARCHAR2(1000),'
        || 'ESTADO                 VARCHAR2(20)  DEFAULT ''VIGENTE'' NOT NULL,'
        || 'BLOQUEADO_POR_PLANILLA NUMBER(1)     DEFAULT 0 NOT NULL,'
        || 'MOTIVO_ANULACION       VARCHAR2(500),'
        || 'ANULADO_POR            VARCHAR2(100),'
        || 'ANULADO_EN             TIMESTAMP,'
        || 'CREADO_POR             VARCHAR2(100) NOT NULL,'
        || 'CREADO_EN              TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,'
        || 'MODIFICADO_POR         VARCHAR2(100),'
        || 'MODIFICADO_EN          TIMESTAMP,'
        || 'CONSTRAINT INDECI_ESSALUD_VIG_PK      PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_ESSALUD_VIG_ESTADO_CK CHECK (ESTADO IN (''VIGENTE'',''PROGRAMADO'',''CERRADO'',''ANULADO'')),'
        || 'CONSTRAINT INDECI_ESSALUD_VIG_BLOQ_CK   CHECK (BLOQUEADO_POR_PLANILLA IN (0,1))'
        || ')'
    );

    DBMS_OUTPUT.PUT_LINE('V010_74 tabla lista.');
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('ERROR V010_74 (tabla): ' || SQLERRM);
        RAISE;
END;
