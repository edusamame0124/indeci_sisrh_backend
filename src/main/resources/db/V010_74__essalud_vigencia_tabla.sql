-- ----------------------------------------------------------------------------
-- 2) Indices
-- ----------------------------------------------------------------------------
DECLARE
    PROCEDURE crear_indice_si_falta(
        p_index_name VARCHAR2,
        p_table_name VARCHAR2,
        p_columns    VARCHAR2
    ) IS
        v_exists NUMBER;
        v_ts     VARCHAR2(30);
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_INDEXES
         WHERE INDEX_NAME = UPPER(p_index_name);
        IF v_exists > 0 THEN
            DBMS_OUTPUT.PUT_LINE(p_index_name || ' ya existe. Sin cambios.');
            RETURN;
        END IF;

        SELECT TABLESPACE_NAME INTO v_ts
          FROM USER_TABLES
         WHERE TABLE_NAME = UPPER(p_table_name);

        EXECUTE IMMEDIATE
            'CREATE INDEX ' || p_index_name
            || ' ON ' || p_table_name || ' ' || p_columns
            || ' TABLESPACE ' || v_ts;
        DBMS_OUTPUT.PUT_LINE(p_index_name || ' -> creado en TBS ' || v_ts);
    END;

BEGIN
    crear_indice_si_falta(
        'INDECI_ESSALUD_VIG_ANIO_IDX',
        'INDECI_ESSALUD_VIGENCIA',
        '(ANIO_VIGENCIA)'
    );
    crear_indice_si_falta(
        'INDECI_ESSALUD_VIG_ESTADO_IDX',
        'INDECI_ESSALUD_VIGENCIA',
        '(ESTADO)'
    );
    crear_indice_si_falta(
        'INDECI_ESSALUD_VIG_FECHA_IDX',
        'INDECI_ESSALUD_VIGENCIA',
        '(VIGENCIA_INICIO, VIGENCIA_FIN)'
    );

    DBMS_OUTPUT.PUT_LINE('V010_74 indices listos.');
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('ERROR V010_74 (indices): ' || SQLERRM);
        RAISE;
END;
