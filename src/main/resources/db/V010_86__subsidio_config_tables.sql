-- Tablas de configuración versionada: parámetros, reglas, catálogos, historial.
-- ============================================================================
-- Idempotente. Oracle 19c+. Schema conectado (GESTIONRRHH).
SET SERVEROUTPUT ON;

DECLARE
    v_ts VARCHAR2(30);
    v_ts_exists NUMBER;
    v_ts_clause VARCHAR2(50);
    PROCEDURE add_table_if_missing(p_table_name VARCHAR2, p_create_ddl VARCHAR2) IS
        v_exists NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_exists FROM USER_TABLES WHERE TABLE_NAME = p_table_name;
        IF v_exists = 0 THEN
            EXECUTE IMMEDIATE p_create_ddl || v_ts_clause;
            DBMS_OUTPUT.PUT_LINE(p_table_name || ' -> creada.');
        ELSE
            DBMS_OUTPUT.PUT_LINE(p_table_name || ' ya existe.');
        END IF;
    END;
    PROCEDURE add_index_if_missing(p_index_name VARCHAR2, p_ddl VARCHAR2) IS
        v_exists NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_exists FROM USER_INDEXES WHERE INDEX_NAME = p_index_name;
        IF v_exists = 0 THEN
            EXECUTE IMMEDIATE p_ddl;
            DBMS_OUTPUT.PUT_LINE(p_index_name || ' -> creado.');
        END IF;
    END;
BEGIN
    -- Ancla: tablespace de INDECI_EMPLEADO solo si existe en la instancia (evita ORA-00959).
    BEGIN
        SELECT TABLESPACE_NAME INTO v_ts
          FROM USER_TABLES
         WHERE TABLE_NAME = 'INDECI_EMPLEADO';
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            v_ts := NULL;
    END;

    IF v_ts IS NOT NULL THEN
        SELECT COUNT(*) INTO v_ts_exists
          FROM USER_TABLESPACES
         WHERE TABLESPACE_NAME = v_ts;
        IF v_ts_exists = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Tablespace ancla ' || v_ts || ' no existe; se intenta default del usuario.');
            v_ts := NULL;
        END IF;
    END IF;

    IF v_ts IS NULL THEN
        SELECT DEFAULT_TABLESPACE INTO v_ts
          FROM USER_USERS
         WHERE USERNAME = USER;
        SELECT COUNT(*) INTO v_ts_exists
          FROM USER_TABLESPACES
         WHERE TABLESPACE_NAME = v_ts;
        IF v_ts_exists = 0 THEN
            DBMS_OUTPUT.PUT_LINE('Default tablespace ' || v_ts || ' no disponible; CREATE sin TABLESPACE.');
            v_ts := NULL;
        END IF;
    END IF;

    IF v_ts IS NOT NULL THEN
        v_ts_clause := ' TABLESPACE ' || v_ts;
        DBMS_OUTPUT.PUT_LINE('Tablespace para tablas subsidio: ' || v_ts);
    ELSE
        v_ts_clause := '';
    END IF;

    add_table_if_missing('INDECI_SUBSIDIO_PARAMETRO',
        'CREATE TABLE INDECI_SUBSIDIO_PARAMETRO ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'CODIGO VARCHAR2(50 CHAR) NOT NULL,'
        || 'NOMBRE VARCHAR2(150 CHAR) NOT NULL,'
        || 'TIPO_VALOR VARCHAR2(20 CHAR) NOT NULL,'
        || 'DESCRIPCION VARCHAR2(500 CHAR),'
        || 'ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,'
        || 'CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        || 'CONSTRAINT INDECI_SUB_PARAM_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_SUB_PARAM_COD_UK UNIQUE (CODIGO)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_PARAMETRO_VERSION',
        'CREATE TABLE INDECI_SUBSIDIO_PARAMETRO_VERSION ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'PARAMETRO_ID NUMBER(19) NOT NULL,'
        || 'VALOR_NUMERICO NUMBER(18,6),'
        || 'VALOR_TEXTO VARCHAR2(500 CHAR),'
        || 'FECHA_VIG_INI DATE NOT NULL,'
        || 'FECHA_VIG_FIN DATE,'
        || 'ESTADO VARCHAR2(20 CHAR) DEFAULT ''BORRADOR'' NOT NULL,'
        || 'FUENTE_NORMATIVA VARCHAR2(200 CHAR),'
        || 'MOTIVO_CAMBIO VARCHAR2(500 CHAR),'
        || 'CREATED_BY VARCHAR2(60 CHAR),'
        || 'APROBADO_BY VARCHAR2(60 CHAR),'
        || 'APROBADO_AT TIMESTAMP,'
        || 'CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        || 'CONSTRAINT INDECI_SUB_PARAM_VER_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT FK_SUB_PARAM_VER FOREIGN KEY (PARAMETRO_ID) REFERENCES INDECI_SUBSIDIO_PARAMETRO (ID)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_REGLA_VIGENCIA',
        'CREATE TABLE INDECI_SUBSIDIO_REGLA_VIGENCIA ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'CODIGO VARCHAR2(50 CHAR) NOT NULL,'
        || 'VERSION VARCHAR2(20 CHAR) NOT NULL,'
        || 'FECHA_VIG_INI DATE NOT NULL,'
        || 'FECHA_VIG_FIN DATE,'
        || 'ESTADO VARCHAR2(20 CHAR) DEFAULT ''BORRADOR'' NOT NULL,'
        || 'DESCRIPCION VARCHAR2(500 CHAR),'
        || 'CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        || 'CONSTRAINT INDECI_SUB_REG_VIG_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_SUB_REG_VIG_UK UNIQUE (CODIGO, VERSION)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_REGLA_CONCEPTO',
        'CREATE TABLE INDECI_SUBSIDIO_REGLA_CONCEPTO ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'REGLA_VIGENCIA_ID NUMBER(19) NOT NULL,'
        || 'TIPO_SUBSIDIO VARCHAR2(20 CHAR) NOT NULL,'
        || 'CONCEPTO_PLANILLA_ID NUMBER(19) NOT NULL,'
        || 'CODIGO_PLAME VARCHAR2(10 CHAR),'
        || 'TIPO_IMPUTACION VARCHAR2(30 CHAR) NOT NULL,'
        || 'ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,'
        || 'CONSTRAINT INDECI_SUB_REG_CON_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT FK_SUB_REG_CON FOREIGN KEY (REGLA_VIGENCIA_ID) REFERENCES INDECI_SUBSIDIO_REGLA_VIGENCIA (ID)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_REGLA_FORMULA',
        'CREATE TABLE INDECI_SUBSIDIO_REGLA_FORMULA ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'REGLA_VIGENCIA_ID NUMBER(19) NOT NULL,'
        || 'CODIGO_FORMULA VARCHAR2(50 CHAR) NOT NULL,'
        || 'EXPRESION_JSON CLOB NOT NULL,'
        || 'DESCRIPCION VARCHAR2(300 CHAR),'
        || 'ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,'
        || 'CONSTRAINT INDECI_SUB_REG_FOR_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT FK_SUB_REG_FOR FOREIGN KEY (REGLA_VIGENCIA_ID) REFERENCES INDECI_SUBSIDIO_REGLA_VIGENCIA (ID)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_REGLA_VALIDACION',
        'CREATE TABLE INDECI_SUBSIDIO_REGLA_VALIDACION ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'REGLA_VIGENCIA_ID NUMBER(19) NOT NULL,'
        || 'CODIGO VARCHAR2(20 CHAR) NOT NULL,'
        || 'EXPRESION_JSON CLOB,'
        || 'SEVERIDAD VARCHAR2(20 CHAR) NOT NULL,'
        || 'MENSAJE_USUARIO VARCHAR2(500 CHAR),'
        || 'ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,'
        || 'CONSTRAINT INDECI_SUB_REG_VAL_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT FK_SUB_REG_VAL FOREIGN KEY (REGLA_VIGENCIA_ID) REFERENCES INDECI_SUBSIDIO_REGLA_VIGENCIA (ID)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_REGLA_APROBACION',
        'CREATE TABLE INDECI_SUBSIDIO_REGLA_APROBACION ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'ENTIDAD_CONFIG VARCHAR2(50 CHAR) NOT NULL,'
        || 'ENTIDAD_ID NUMBER(19) NOT NULL,'
        || 'REGISTRADO_POR VARCHAR2(60 CHAR) NOT NULL,'
        || 'APROBADO_POR VARCHAR2(60 CHAR),'
        || 'ESTADO VARCHAR2(20 CHAR) DEFAULT ''PENDIENTE'' NOT NULL,'
        || 'MOTIVO VARCHAR2(500 CHAR),'
        || 'CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        || 'APROBADO_AT TIMESTAMP,'
        || 'CONSTRAINT INDECI_SUB_REG_APR_PK PRIMARY KEY (ID)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_HISTORIAL_CAMBIO',
        'CREATE TABLE INDECI_SUBSIDIO_HISTORIAL_CAMBIO ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'ENTIDAD VARCHAR2(50 CHAR) NOT NULL,'
        || 'ENTIDAD_ID NUMBER(19) NOT NULL,'
        || 'CAMPO VARCHAR2(50 CHAR) NOT NULL,'
        || 'VALOR_ANTES VARCHAR2(1000 CHAR),'
        || 'VALOR_DESPUES VARCHAR2(1000 CHAR),'
        || 'USUARIO VARCHAR2(60 CHAR),'
        || 'MOTIVO VARCHAR2(500 CHAR),'
        || 'CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        || 'CONSTRAINT INDECI_SUB_HIST_PK PRIMARY KEY (ID)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_CATALOGO',
        'CREATE TABLE INDECI_SUBSIDIO_CATALOGO ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'TIPO_CATALOGO VARCHAR2(50 CHAR) NOT NULL,'
        || 'CODIGO VARCHAR2(50 CHAR) NOT NULL,'
        || 'NOMBRE VARCHAR2(150 CHAR) NOT NULL,'
        || 'ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,'
        || 'ORDEN_VISUAL NUMBER(5) DEFAULT 100,'
        || 'CONSTRAINT INDECI_SUB_CAT_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT INDECI_SUB_CAT_UK UNIQUE (TIPO_CATALOGO, CODIGO)'
        || ')');

    add_table_if_missing('INDECI_SUBSIDIO_CATALOGO_DETALLE',
        'CREATE TABLE INDECI_SUBSIDIO_CATALOGO_DETALLE ('
        || 'ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,'
        || 'CATALOGO_ID NUMBER(19) NOT NULL,'
        || 'CODIGO VARCHAR2(50 CHAR) NOT NULL,'
        || 'ETIQUETA VARCHAR2(150 CHAR) NOT NULL,'
        || 'METADATA_JSON CLOB,'
        || 'ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,'
        || 'ORDEN_VISUAL NUMBER(5) DEFAULT 100,'
        || 'CONSTRAINT INDECI_SUB_CAT_DET_PK PRIMARY KEY (ID),'
        || 'CONSTRAINT FK_SUB_CAT_DET FOREIGN KEY (CATALOGO_ID) REFERENCES INDECI_SUBSIDIO_CATALOGO (ID)'
        || ')');

END;
