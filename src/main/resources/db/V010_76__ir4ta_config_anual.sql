DECLARE
    v_ts     VARCHAR2(30);
    v_exists NUMBER;
    v_rows   NUMBER;

    PROCEDURE eliminar_tabla_vacia IS
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_TABLES
         WHERE TABLE_NAME = 'INDECI_IR4TA_CONFIG_ANUAL';
        IF v_exists = 0 THEN RETURN; END IF;

        EXECUTE IMMEDIATE 'SELECT COUNT(*) FROM INDECI_IR4TA_CONFIG_ANUAL' INTO v_rows;
        IF v_rows > 0 THEN
            DBMS_OUTPUT.PUT_LINE('INDECI_IR4TA_CONFIG_ANUAL tiene ' || v_rows || ' filas. No se elimina.');
            RETURN;
        END IF;
        EXECUTE IMMEDIATE 'DROP TABLE INDECI_IR4TA_CONFIG_ANUAL CASCADE CONSTRAINTS PURGE';
        DBMS_OUTPUT.PUT_LINE('Tabla vacia eliminada (CASCADE CONSTRAINTS).');
    END;

