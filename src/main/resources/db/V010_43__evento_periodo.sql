DECLARE
    v_tipos       NUMBER;
    v_eventos     NUMBER;
    v_uk_existe   NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_tipos
      FROM INDECI_TIPO_EVENTO
     WHERE ACTIVO = 1;
