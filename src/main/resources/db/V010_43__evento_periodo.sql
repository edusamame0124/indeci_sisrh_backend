        ||   'CODIGO                   VARCHAR2(30 CHAR) NOT NULL,'
        ||   'NOMBRE                   VARCHAR2(100 CHAR) NOT NULL,'
        ||   'AFECTA_DIAS_LABORADOS    VARCHAR2(1 CHAR) DEFAULT ''N'' NOT NULL,'
        ||   'AFECTA_BASE_AFP          VARCHAR2(1 CHAR) DEFAULT ''S'' NOT NULL,'
        ||   'AFECTA_BASE_ESSALUD      VARCHAR2(1 CHAR) DEFAULT ''S'' NOT NULL,'
        ||   'GENERA_SUBSIDIO          VARCHAR2(1 CHAR) DEFAULT ''N'' NOT NULL,'
        ||   'REQUIERE_ADJUNTO         VARCHAR2(1 CHAR) DEFAULT ''N'' NOT NULL,'
        ||   'PERMITE_SOLAPE           VARCHAR2(1 CHAR) DEFAULT ''N'' NOT NULL,'
        ||   'CODIGO_PLAME_SUNAT       VARCHAR2(6 CHAR),'
        ||   'ORDEN_VISUAL             NUMBER(3) DEFAULT 100 NOT NULL,'
        ||   'ACTIVO                   NUMBER(1) DEFAULT 1 NOT NULL,'
        ||   'CREATED_AT               TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_PK PRIMARY KEY (ID),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_CODIGO_UK UNIQUE (CODIGO),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_AFEC_DIAS_CK CHECK (AFECTA_DIAS_LABORADOS IN (''S'',''N'')),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_AFEC_AFP_CK  CHECK (AFECTA_BASE_AFP IN (''S'',''N'')),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_AFEC_ESS_CK  CHECK (AFECTA_BASE_ESSALUD IN (''S'',''N'')),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_SUBS_CK      CHECK (GENERA_SUBSIDIO IN (''S'',''N'')),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_ADJ_CK       CHECK (REQUIERE_ADJUNTO IN (''S'',''N'')),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_SOLAPE_CK    CHECK (PERMITE_SOLAPE IN (''S'',''N'')),'
        ||   'CONSTRAINT INDECI_TIPO_EVENTO_ACTIVO_CK    CHECK (ACTIVO IN (0,1))'
