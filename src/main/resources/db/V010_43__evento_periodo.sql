-- ----------------------------------------------------------------------------
-- 3) Índices de soporte para queries del motor y del repo.
-- ----------------------------------------------------------------------------
DECLARE
    v_ts VARCHAR2(30);
    PROCEDURE add_ix_if_missing(
        p_index_name VARCHAR2,
        p_table_name VARCHAR2,
        p_columns    VARCHAR2
    ) IS
        v_exists NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_INDEXES
         WHERE INDEX_NAME = p_index_name;
        IF v_exists > 0 THEN
            DBMS_OUTPUT.PUT_LINE(p_index_name || ' ya existe. Sin cambios.');
            RETURN;
        END IF;
        EXECUTE IMMEDIATE
            'CREATE INDEX ' || p_index_name
            || ' ON ' || p_table_name || ' ' || p_columns
            || ' TABLESPACE ' || v_ts;
        DBMS_OUTPUT.PUT_LINE(p_index_name || ' -> creado en TBS ' || v_ts);
    END;
BEGIN
    SELECT TABLESPACE_NAME INTO v_ts
      FROM USER_TABLES
     WHERE TABLE_NAME = 'INDECI_EMPLEADO';

    -- Motor PASO 3: busca eventos vigentes por empleado + período activos.
    add_ix_if_missing(
        'INDECI_EMP_EVENTO_EMP_PER_IX',
        'INDECI_EMPLEADO_EVENTO',
        '(EMPLEADO_ID, PERIODO, ACTIVO)'
    );
    -- Repo: listar eventos por empleado (timeline).
    add_ix_if_missing(
        'INDECI_EMP_EVENTO_EMP_IX',
        'INDECI_EMPLEADO_EVENTO',
        '(EMPLEADO_ID, ACTIVO)'
    );
    -- Solape: búsqueda por rango de fechas para validador no-solape.
    add_ix_if_missing(
        'INDECI_EMP_EVENTO_FECHAS_IX',
        'INDECI_EMPLEADO_EVENTO',
        '(EMPLEADO_ID, FECHA_INICIO, FECHA_FIN, ACTIVO)'
    );
END;
