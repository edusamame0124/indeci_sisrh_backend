      FROM USER_CONSTRAINTS
     WHERE CONSTRAINT_NAME = 'INDECI_EMP_EVENTO_UK';

    DBMS_OUTPUT.PUT_LINE('---');
    DBMS_OUTPUT.PUT_LINE('Tipos de evento activos     : ' || v_tipos     || ' (esperado 10)');
    DBMS_OUTPUT.PUT_LINE('INDECI_EMPLEADO_EVENTO filas: ' || v_eventos   || ' (esperado 0 fresh install)');
    DBMS_OUTPUT.PUT_LINE('UK INDECI_EMP_EVENTO_UK     : ' || v_uk_existe || ' (esperado 1)');
    DBMS_OUTPUT.PUT_LINE('V010_43 listo.');
END;
