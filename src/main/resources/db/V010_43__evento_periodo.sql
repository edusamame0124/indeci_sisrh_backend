-- ----------------------------------------------------------------------------
-- 4) Seed catálogo INDECI_TIPO_EVENTO (idempotente, MERGE).
--    Flags por tipo según normativa peruana:
--
--    AFECTA_DIAS_LAB: ¿resta del default 30 que cuenta el motor?
--      MATERNIDAD/ENFERMEDAD: S (no labora durante el descanso, días subsidiados)
--      LICENCIA_GOCE: N (con goce → laborado fiscal, no descuenta)
--      LICENCIA_SIN_GOCE: S
--      LACTANCIA: N (es 1 hora diaria, no día completo)
--      PATERNIDAD: N (licencia con goce de haber)
--      CESE: S (días desde fechaCese hasta fin de mes no laborados)
--      REINTEGRO: N (es un "+ días", lo maneja F1.4 INDECI_EMPLEADO_REINTEGRO)
--      FERIADO_COMPENSABLE: N (es día compensado)
--      PERMISO_PERSONAL: S
--      FALTA_INJUSTIFICADA: S (alternativa a AsistenciaCabecera.diasFalta)
--
--    AFECTA_BASE_AFP / AFECTA_BASE_ESSALUD: S por default (la base se mantiene
--      durante eventos remunerados). Maternidad/enfermedad: el SUBSIDIO no afecta
--      base — pero esto se maneja en F2.4 (SubsidioCalculadorService), no aquí.
--
--    GENERA_SUBSIDIO: solo MATERNIDAD y ENFERMEDAD.
--
--    REQUIERE_ADJUNTO: maternidad, enfermedad, licencias, suspensión, cese
--      (siempre hay constancia/certificado).
--
--    PERMITE_SOLAPE: solo LACTANCIA (parcial puede solapar maternidad).
-- ----------------------------------------------------------------------------
MERGE INTO INDECI_TIPO_EVENTO d
USING (
    SELECT 'MATERNIDAD'           AS CODIGO, 'Subsidio por Maternidad'        AS NOMBRE,
           'S' AS AFECTA_DIAS_LABORADOS, 'S' AS AFECTA_BASE_AFP, 'S' AS AFECTA_BASE_ESSALUD,
           'S' AS GENERA_SUBSIDIO, 'S' AS REQUIERE_ADJUNTO, 'N' AS PERMITE_SOLAPE,
           10  AS ORDEN_VISUAL FROM DUAL UNION ALL
    SELECT 'ENFERMEDAD',           'Subsidio por Enfermedad',
           'S','S','S','S','S','N', 20 FROM DUAL UNION ALL
    SELECT 'LICENCIA_GOCE',        'Licencia con goce de haber',
           'N','S','S','N','S','N', 30 FROM DUAL UNION ALL
    SELECT 'LICENCIA_SIN_GOCE',    'Licencia sin goce de haber',
           'S','N','N','N','S','N', 40 FROM DUAL UNION ALL
    SELECT 'LACTANCIA',            'Permiso de Lactancia (Ley 27240)',
           'N','S','S','N','N','S', 50 FROM DUAL UNION ALL
    SELECT 'PATERNIDAD',           'Licencia por Paternidad (Ley 29409)',
           'N','S','S','N','S','N', 60 FROM DUAL UNION ALL
    SELECT 'CESE',                 'Cese laboral',
           'S','N','N','N','S','N', 70 FROM DUAL UNION ALL
    SELECT 'REINTEGRO',            'Reintegro por días (informativo)',
           'N','S','S','N','N','N', 80 FROM DUAL UNION ALL
    SELECT 'FERIADO_COMPENSABLE',  'Feriado compensable',
           'N','S','S','N','N','N', 90 FROM DUAL UNION ALL
    SELECT 'PERMISO_PERSONAL',     'Permiso personal sin goce',
           'S','N','N','N','S','N', 100 FROM DUAL
) s
ON (d.CODIGO = s.CODIGO)
WHEN NOT MATCHED THEN
    INSERT (CODIGO, NOMBRE,
            AFECTA_DIAS_LABORADOS, AFECTA_BASE_AFP, AFECTA_BASE_ESSALUD,
            GENERA_SUBSIDIO, REQUIERE_ADJUNTO, PERMITE_SOLAPE,
            ORDEN_VISUAL, ACTIVO, CREATED_AT)
    VALUES (s.CODIGO, s.NOMBRE,
            s.AFECTA_DIAS_LABORADOS, s.AFECTA_BASE_AFP, s.AFECTA_BASE_ESSALUD,
            s.GENERA_SUBSIDIO, s.REQUIERE_ADJUNTO, s.PERMITE_SOLAPE,
            s.ORDEN_VISUAL, 1, SYSTIMESTAMP);

COMMIT;

-- ----------------------------------------------------------------------------
