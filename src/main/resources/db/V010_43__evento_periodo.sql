-- ============================================================================
-- F2.1 / V010_43 — INDECI_TIPO_EVENTO + INDECI_EMPLEADO_EVENTO
--
-- OBJETIVO:
--   Modelar eventos del período que afectan la planilla del empleado:
--   maternidad, enfermedad, licencias, lactancia, paternidad, cese, reintegro,
--   permiso personal, feriado compensable. Hoy el motor solo descuenta días
--   por FALTA (de AsistenciaCabecera) — con F2 también restará días por
--   eventos según el flag AFECTA_DIAS_LABORADOS del tipo.
--
--   Subsidios (maternidad/enfermedad) se calculan en F2.4
--   (SubsidioCalculadorService) usando el promedio de los últimos 12 meses
--   y la diferencia INDECI vs EsSalud.
--
-- DECISIONES DE DISEÑO:
--   - INDECI_TIPO_EVENTO catálogo, con flags por tipo:
--       AFECTA_DIAS_LABORADOS  S/N — resta del default 30 (motor PASO 3)
--       AFECTA_BASE_AFP        S/N — afecta base pensión
--       AFECTA_BASE_ESSALUD    S/N — afecta base ESSALUD
--       GENERA_SUBSIDIO        S/N — dispara SubsidioCalculadorService
--       REQUIERE_ADJUNTO       S/N — Legajo+FTP en F2.6
--       PERMITE_SOLAPE         S/N — lactancia parcial puede solapar maternidad
--   - INDECI_EMPLEADO_EVENTO transaccional:
--       PERIODO VARCHAR2(6) (YYYYMM) coherente con resto del schema.
--       DIAS_AFECTOS derivado por default (fechaFin-fechaInicio+1) pero el
--           usuario puede sobrescribir (días no laborables, casos especiales).
--       SUSTENTO_LEGAJO_DOC_ID FK a INDECI_LEGAJO_DOCUMENTO (V010_35), nullable
--           — el motor no exige adjunto; el service lo exigirá según
--           TipoEvento.requiereAdjunto.
--       ESTADO REGISTRADO|VALIDADO|RECHAZADO — flujo de aprobación queda en UI.
--   - UK por (EMPLEADO_ID, TIPO_EVENTO_ID, FECHA_INICIO) — evita duplicados
--       exactos. Solape de fechas se valida en la app (más flexible).
--   - Tablespace explícito vía INDECI_EMPLEADO (patrón establecido).
--
-- IDEMPOTENTE. Schema NO hardcodeado. Oracle 19c+.
-- ============================================================================

SET SERVEROUTPUT ON;

-- ----------------------------------------------------------------------------
-- 1) Tablas nuevas con tablespace explícito.
-- ----------------------------------------------------------------------------
DECLARE
    v_ts VARCHAR2(30);
    PROCEDURE add_table_if_missing(
        p_table_name VARCHAR2,
        p_create_ddl VARCHAR2
    ) IS
        v_exists NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_exists
          FROM USER_TABLES
         WHERE TABLE_NAME = p_table_name;
