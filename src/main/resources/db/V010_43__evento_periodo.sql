-- ----------------------------------------------------------------------------
-- 2) UK INDECI_EMP_EVENTO_UK — (EMPLEADO_ID, TIPO_EVENTO_ID, FECHA_INICIO).
--    Patrón CREATE UNIQUE INDEX + ADD CONSTRAINT USING INDEX
--    (ver V010_39 — evita ORA-00959 por default tablespace).
-- ----------------------------------------------------------------------------
DECLARE
    v_ts                VARCHAR2(30);
    v_constraint_exists NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_constraint_exists
      FROM USER_CONSTRAINTS
     WHERE CONSTRAINT_NAME = 'INDECI_EMP_EVENTO_UK';

    IF v_constraint_exists > 0 THEN
        DBMS_OUTPUT.PUT_LINE('INDECI_EMP_EVENTO_UK ya existe. Sin cambios.');
        RETURN;
    END IF;

    SELECT TABLESPACE_NAME INTO v_ts
      FROM USER_TABLES
     WHERE TABLE_NAME = 'INDECI_EMPLEADO';

    BEGIN
        EXECUTE IMMEDIATE 'DROP INDEX INDECI_EMP_EVENTO_UK';
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE = -1418 THEN NULL; ELSE RAISE; END IF;
    END;

    EXECUTE IMMEDIATE
        'CREATE UNIQUE INDEX INDECI_EMP_EVENTO_UK '
        || 'ON INDECI_EMPLEADO_EVENTO (EMPLEADO_ID, TIPO_EVENTO_ID, FECHA_INICIO) '
        || 'TABLESPACE ' || v_ts;
    EXECUTE IMMEDIATE
        'ALTER TABLE INDECI_EMPLEADO_EVENTO '
        || 'ADD CONSTRAINT INDECI_EMP_EVENTO_UK '
        || 'UNIQUE (EMPLEADO_ID, TIPO_EVENTO_ID, FECHA_INICIO) '
        || 'USING INDEX INDECI_EMP_EVENTO_UK';
    DBMS_OUTPUT.PUT_LINE('INDECI_EMP_EVENTO_UK -> creado en TBS ' || v_ts);
END;
